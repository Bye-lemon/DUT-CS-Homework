.MODEL TINY
.CODE
.STARTUP
	MOV  DX, OFFSET TIPS
	MOV  AH, 09H
	INT  21H
	
	MOV  DX, OFFSET BUFFER
	MOV  AH, 0AH
	INT  21H
	
ENCODE:	MOV  BP, OFFSET BUFFER
	ADD  BP, 02D
	MOV  CX, 05D
PLUS:	ADD  BYTE PTR [BP], 02D
	CMP  BYTE PTR [BP], 5BH
	JB   GO
	SUB  BYTE PTR [BP], 1AH
GO:	INC  BP
	LOOP PLUS
	MOV  DX, OFFSET EN
	MOV  AH, 09H
	INT  21H
	MOV  DX, OFFSET BUFFER + 2
	MOV  AH, 09H
	INT  21H
	
DECODE: MOV  BP, OFFSET BUFFER
	ADD  BP, 02D
	MOV  CX, 05D
SUBT:	SUB  BYTE PTR [BP], 04D
	CMP  BYTE PTR [BP], 41H
	JA   PASS
	ADD  BYTE PTR [BP], 1AH
PASS:	INC  BP
	LOOP SUBT
	MOV  DX, OFFSET DE
	MOV  AH, 09H
	INT  21H
	MOV  DX, OFFSET BUFFER + 2
	MOV  AH, 09H
	INT  21H
.EXIT 0

TIPS    DB 'PLEASE INPUT A STRING (REQUIRED 5 HIGHER CHARACTERS):', 0DH, 0AH, 24H
EN      DB 'ENCODE:', 24H
DE      DB 'DECODE:', 24H
BUFFER  DB 6
	DB 0
	DB 6 DUP(0)
	DB 0DH, 0AH, 24H

END