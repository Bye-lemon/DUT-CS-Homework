.MODEL TINY
.CODE
.STARTUP
	MOV  DX, OFFSET TIPS
	MOV  AH, 09H
	INT  21H
	
	MOV  DX, OFFSET BUFFER
	MOV  AH, 0AH
	INT  21H
	
	MOV  CX, 00H
	MOV  DX, OFFSET FNAME
	MOV  AH, 3CH
	INT  21H
	MOV  BX, AX
	MOV  AH, 3EH
	INT  21H
	
	MOV  AL, 01H
	MOV  DX, OFFSET FNAME
	MOV  AH, 3DH
	INT  21H
	PUSH AX
	
	MOV  BX, AX
	MOV  DX, OFFSET BUFFER + 2
	MOV  CX, [OFFSET BUFFER + 1]
	MOV  AH, 40H
	INT  21H
	
	POP  BX
	MOV  AH, 3EH
	INT  21H
.EXIT 0
